/// 🔀 Enum representing HTTP methods
enum HTTPMethod: String {
    // 📥 GET method for retrieving data
    case get = "GET"
    
    // 📤 POST method for sending data to the server
    case post = "POST"
    
    // 🛠️ PUT method for updating existing data
    case put = "PUT"
    
    // 🗑️ DELETE method for removing data
    case delete = "DELETE"
}
